public class IntegralsWithFloatOperators {

  public static void main(String[] args) {
    
    System.out.println(2 + 0.5);
    System.out.println(0.5 + 2);

    System.out.println(2 - 0.5);
    System.out.println(0.5 - 2);

    System.out.println(3 * 0.5);
    System.out.println(0.5 * 3);

    System.out.println(2 / 0.5);
    System.out.println(0.5 / 2);

    System.out.println(2 % 0.5);
    System.out.println(0.5 % 2);
  }

}
