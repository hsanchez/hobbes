public class IntegralOperators {
  
  public static void main(String[] args) {
    // Numerical comparison
    System.out.println(1 <  0);
    System.out.println(1 <  1);
    System.out.println(1 <  2);
    System.out.println(1 <= 0);
    System.out.println(1 <= 1);
    System.out.println(1 <= 2);
    System.out.println(1 == 0);
    System.out.println(1 == 1);
    System.out.println(1 == 2);
    System.out.println(1 >  0);
    System.out.println(1 >  1);
    System.out.println(1 >  2);
    System.out.println(1 >= 0);
    System.out.println(1 >= 1);
    System.out.println(1 >= 2);
    System.out.println(1 != 0);
    System.out.println(1 != 1);
    System.out.println(1 != 2);
  }

}
