public class IntegralTypes {

  public static void main(String[] args) {
    
    byte fitsByte = 5;
    System.out.println(fitsByte);

    short fitsShort = 5000;
    System.out.println(fitsShort);

    int fitsInt = 50000;
    System.out.println(fitsInt);

    long fitsLong = 50000000000L;
    System.out.println(fitsLong);

  }

}
