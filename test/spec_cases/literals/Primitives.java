public class Primitives {
  
  public static void main(String[] args) {
    
    // boolean literals
    boolean trueLiteral = true,
            falseLiteral = false;
  
    // Integer literals
    int one = 1,
        minusOne = +1,
        plusOne = -1;
    
    // Floating Point literals
    float two = 2f,
          pointTwo = .2f,
          minusPointTwo = -.2f,
          plusPointTwo = +.2f,
          zeroPointTwo = 0.2f,
          minusZeroPointTwo = -0.2f,
          plusZeroPointTwo = +0.2f;
          
  }

}
